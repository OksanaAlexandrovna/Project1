class Ability {
    fun earlyHunt () {
        println("Early hunting at any level of sanity") // раняя охота на любом уровне расудка
    }

    fun huntingMinute () {
        println("The hunt begins a minute after using the incense") // Охота начинается спустя минуту после использования благовония
    }

    fun bansheeScream () {
        println("There is a 33% chance that the banshee will scream into the directional microphone") // с шансом 33% банши кричит в направленный микрофон
    }

    fun pursuit () {
        println("The banshee chooses a victim and follows it") // банши выбирает себе жертву и ходит за ней
    }

    fun uniqueAnswer () {
        println("33% chance that he will give a unique response in the form of heavy breathing") // 33% шанс, что он выдаст уникальный ответ в виде тяжёлого дыхания
    }

    fun copyGhostAbility () {
        println("The Mimic is able to copy any type of ghost, completely copying its features") // Мимик способен скопировать любой тип призрака, полностью копируя его особенности
    }

    fun invisibility () {
        println("If you take a photo of a phantom, it won’t be in the photo.") // Если сфотографировать фантома то на фото его не будет
    }

    fun hunt3Minutes () {
        println("After using incense, the spirit cannot start hunting for 3 minutes") // После использования благовония дух не может начать охоту 3 минуты
    }

    fun closingTheDoors () {
        println("The ghost can completely close the door") // Призрак может полностью закрыть дверь
    }

}