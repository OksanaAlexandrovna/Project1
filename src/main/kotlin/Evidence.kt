class Evidence {
    fun ghostOrb() {
        println("a ghost orb is visible on the camera") // призрачный огонек виден на камере
    }

    fun ultraviolet() {
        println("hand or foot prints left by a ghost") // отпечатки рук или ног оставленные призраком
    }

    fun ghostWriting() {
        println("the ghost left a note in a notebook") // призрак оставил запись в блокноте
    }

    fun freezingTemperatures() {
        println("sub-zero temperature in the ghost room") // минусовая температура в комнате призрака
    }

    fun spiritBox() {
        println("the ghost answered the radio") // призрак ответил в радиоприемник
    }

    fun emfLevel5() {
        println("EMF detector shows level 5") // детектор ЭМП показывает 5 уровень
    }

    fun laserProjector() {
        println("a ghost appeared in a laser projector") // призрак появился в лазерном проекторе
    }
}